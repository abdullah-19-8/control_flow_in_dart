void main(List<String> args) {
  const yourName = 'John';
  const myName = 'Abdullah';

  // if and else statements in dart check the condition and execute the code
  // if the condition is true, otherwise it will execute the code in the else
  // if first one is false, and if there is no else, it will do nothing.
  if (yourName == myName) {
    print('We have the same name');
  } else if (yourName == 'John') {
    print('Your name is John');
  } else {
    print('We have different names');
  }
}
